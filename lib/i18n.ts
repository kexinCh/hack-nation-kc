export function getStoredLanguage(): PreferredLanguage {
  if (typeof window === "undefined") return "english";
  const value = window.localStorage.getItem(LANGUAGE_KEY);
  return value === "spanish" || value === "chinese" ? value : "english";
}

export function setStoredLanguage(language: PreferredLanguage) {
  window.localStorage.setItem(LANGUAGE_KEY, language);
  window.dispatchEvent(new CustomEvent(LANGUAGE_EVENT, { detail: language }));
}

function format(template: string, values: Record<string, string | number> = {}) {
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(values[key] ?? `{${key}}`));
}

export function translate(
  language: PreferredLanguage,
  key: keyof typeof english | string,
  values?: Record<string, string | number>,
) {
  const localized = dictionaries[language] as Dictionary;
  const fallback = english as Dictionary;
  const template = localized[key] ?? fallback[key] ?? key;
  return format(template, values);
}

export function useTranslations() {
  const [language, setLanguageState] =
    useState<PreferredLanguage>("english");

  useEffect(() => {
    const syncStoredLanguage = () => {
      setLanguageState(getStoredLanguage());
    };

    // Load the saved language only after hydration.
    syncStoredLanguage();

    window.addEventListener(LANGUAGE_EVENT, syncStoredLanguage);
    window.addEventListener("storage", syncStoredLanguage);

    return () => {
      window.removeEventListener(LANGUAGE_EVENT, syncStoredLanguage);
      window.removeEventListener("storage", syncStoredLanguage);
    };
  }, []);

  const setLanguage = (nextLanguage: PreferredLanguage) => {
    setLanguageState(nextLanguage);
    setStoredLanguage(nextLanguage);
  };

  const tr = (
    key: keyof typeof english | string,
    values?: Record<string, string | number>,
  ) => translate(language, key, values);

  return {
    language,
    setLanguage,
    tr,
    t: new Proxy(english, {
      get(_target, key: string) {
        return tr(key);
      },
    }) as typeof english,
    documentTypeLabel: (type: DocumentType) =>
      tr(documentTypeKeys[type]),
    uploadLabel: (type: DocumentType) =>
      tr(uploadKeys[type]),
    requestTitle: (type: DocumentType) =>
      tr(requestTitleKeys[type]),
    requestDescription: (type: DocumentType) =>
      tr(requestDescriptionKeys[type]),
    incomeSourceLabel: (source: IncomeSource) =>
      tr(incomeKeys[source][0]),
    incomeSourceDescription: (source: IncomeSource) =>
      tr(incomeKeys[source][1]),
    statusLabel: (status: DocumentStatus | string) =>
      tr(statusKeys[status] ?? status),
  };
}
